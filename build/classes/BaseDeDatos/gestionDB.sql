CREATE TABLE ESTUDIANTE (
CEDULA VARCHAR(12) NOT NULL,
NOMBRE VARCHAR(255),
APELLIDO VARCHAR(255),
CORREO VARCHAR(255),
TELEFONO INT,
COD_CARRERA VARCHAR(12),
PRIMARY KEY (CEDULA)
);


CREATE TABLE PROFESOR (
CEDULA VARCHAR(12) NOT NULL,
NOMBRE VARCHAR(255),
APELLIDO VARCHAR(255),
CORREO VARCHAR(255),
TELEFONO INT,
PRIMARY KEY (CEDULA)
);

CREATE TABLE CURSO (
CODIGO VARCHAR(12) NOT NULL,
NOMBRE VARCHAR(255),
CREDITOS INT,
H_SEMANALES INT,
COD_CARRERA VARCHAR(12),
NUM_CICLO INT NOT NULL,
PRIMARY KEY (CODIGO)
);

CREATE TABLE CICLO (
ID INT NOT NULL,
ANNO INT NOT NULL,
NUM_CICLO INT NOT NULL,
FECHA_INICIO VARCHAR(10),
FECHA_FINAL VARCHAR(10),
PRIMARY KEY (ID)
);


CREATE TABLE CARRERA (
CODIGO VARCHAR(12) NOT NULL,
NOMBRE VARCHAR(255) NOT NULL,
PRIMARY KEY (CODIGO)
);


CREATE TABLE USUARIO (
USUARIO INT NOT NULL,
PASSWORD VARCHAR(255),
TIPO INT NOT NULL,
PRIMARY KEY (USUARIO),
);

CREATE TABLE GRUPO (
NUMERO INT NOT NULL,
ID_PROF VARCHAR(12),
COD_CURSO VARCHAR(12),
);

CREATE TABLE MATRICULADOR(
    CEDULA VARCHAR(12) NOT NULL,
  NOMBRE VARCHAR(255),
  APELLIDO VARCHAR(255),
  CORREO VARCHAR(255),
  TELEFONO INT,
  PRIMARY KEY (CEDULA)
);

CREATE TABLE ADMINISTRADOR (
    CEDULA VARCHAR(12) NOT NULL,
  NOMBRE VARCHAR(255),
  APELLIDO VARCHAR(255),
  CORREO VARCHAR(255),
  TELEFONO INT,
  PRIMARY KEY (CEDULA)
);
